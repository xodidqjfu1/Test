//패키지 선언
package java01_variables;

//클래스 정의
public class Variables_01 { //_C001_Start

	//메인 메소드 정의
	public static void main(String[] args) { //_M001_Start
		
		System.out.println("12345"); //정수 : 소수점 이하가 없는 숫자
		
		System.out.println("398.000");	//실수 : 소수점 이하가 있는 숫자
		
		System.out.println(true); //논리 : 참/거짓 (true/false) _주황색(자바가 정해놓은 고유명사)
		
		System.out.println('A'); //단일 문자 : 1개 글자 표현 
		
		System.out.println((char)66); // char(캐릭터)로 인식
		System.out.println((char)44032); // 10진법 유니코드
		
		System.out.println((int)'a'); // int(정수)로 인식

		//--------------------------------------------------
		
		System.out.println( "Hola" ); //문자열 : 0개 이상의 글자 표현

	} //_M001_End

} //_COO1_End
